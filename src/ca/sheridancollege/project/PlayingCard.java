package ca.sheridancollege.project;

/**
 *
 * @author 20pri
 */
public class PlayingCard extends Card {
    private final String suit;
    private final int value;

    public PlayingCard(String suit, int value) {
        this.suit = suit;
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    @Override
    public String toString() {
        return suit + " " + value;
    }
}